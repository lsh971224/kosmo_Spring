package com.study.springboot;

public class PersonDTO {
	private String name;
	private String age;
	
	//Getter,Setter 생성
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	
}
